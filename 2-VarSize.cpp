#include <iostream>
using namespace std;
int main()
{
	int a;long int b; long long int c; bool d; char e; unsigned char f; float g; double h; long double i;
	cout<<"int: "<<sizeof(a)<<endl
	<<"long int: "<<sizeof(b)<<endl
	<<"long long int: "<<sizeof(c)<<endl
	<<"bool: "<<sizeof(d)<<endl
	<<"char: "<<sizeof(e)<<endl
	<<"unsigned char: "<<sizeof(f)<<endl
	<<"float: "<<sizeof(g)<<endl
	<<"double: "<<sizeof(h)<<endl
	<<"long double: "<<sizeof(i)<<endl
	;
	
	return 0;
}
